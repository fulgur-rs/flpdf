# Phase 3: Size check (fail if any reference exceeds 100 KB)
# ---------------------------------------------------------------------------

echo ""
echo "=== Size check (limit: 100 KB per file) ==="
LIMIT=$((100 * 1024))
FAILED=0
# Portable file size: GNU stat uses -c%s, BSD/macOS stat uses -f%z.
file_size() {
    if size=$(stat -c%s "$1" 2>/dev/null); then
        printf '%s' "$size"
    else
        stat -f%z "$1"
    fi
}
while IFS= read -r -d '' f; do
    size=$(file_size "$f")
    rel="${f#"$ROOT"/}"
    if [[ "$size" -gt "$LIMIT" ]]; then
        echo "OVER LIMIT: $rel ($size bytes)"
        FAILED=1
    else
        echo "OK: $rel ($size bytes)"
    fi
done < <(find "$REF" -name "*.pdf" -print0 | sort -z)

if [[ "$FAILED" -eq 1 ]]; then
    echo ""
    echo "ERROR: One or more reference files exceed 100 KB limit." >&2
    exit 1
fi

echo ""
echo "Done."
