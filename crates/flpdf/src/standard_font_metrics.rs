/// One of the 14 standard PDF fonts.
///
/// Use [`StandardFont::from_base_name`] to resolve both official PDF BaseFont
/// names and the short AcroForm aliases (e.g. `Helv`, `TiRo`, `ZaDb`).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum StandardFont {
    /// Helvetica (AcroForm alias: `Helv`)
    Helvetica,
    /// Helvetica-Bold (AcroForm alias: `HeBo`)
    HelveticaBold,
    /// Helvetica-Oblique (AcroForm alias: `HeOb`)
    HelveticaOblique,
    /// Helvetica-BoldOblique (AcroForm alias: `HeBO`)
    HelveticaBoldOblique,
    /// Times-Roman (AcroForm alias: `TiRo`)
    TimesRoman,
    /// Times-Bold (AcroForm alias: `TiBo`)
    TimesBold,
    /// Times-Italic (AcroForm alias: `TiIt`)
    TimesItalic,
    /// Times-BoldItalic (AcroForm alias: `TiBI`)
    TimesBoldItalic,
    /// Courier (AcroForm alias: `Cour`)
    Courier,
    /// Courier-Bold (AcroForm alias: `CoBo`)
    CourierBold,
    /// Courier-Oblique (AcroForm alias: `CoOb`)
    CourierOblique,
    /// Courier-BoldOblique (AcroForm alias: `CoBO`)
    CourierBoldOblique,
    /// Symbol (AcroForm alias: `Symb`)
    Symbol,
    /// ZapfDingbats (AcroForm alias: `ZaDb`)
    ZapfDingbats,
}

impl StandardFont {
    /// Resolve a PDF BaseFont name or AcroForm short alias to a [`StandardFont`].
    ///
    /// Recognises both official names (e.g. `b"Helvetica-Bold"`) and the
    /// four-character AcroForm aliases defined in PDF 32000-1:2008 §12.7.3.3
    /// (e.g. `b"HeBo"`).  Matching is case-sensitive, as required by the PDF spec.
    ///
    /// Returns `None` for unrecognised names.
    pub fn from_base_name(name: &[u8]) -> Option<StandardFont> {
        match name {
            b"Helv" => Some(StandardFont::Helvetica),
            b"Helvetica" => Some(StandardFont::Helvetica),
            b"HeBo" => Some(StandardFont::HelveticaBold),
            b"Helvetica-Bold" => Some(StandardFont::HelveticaBold),
            b"HeOb" => Some(StandardFont::HelveticaOblique),
            b"Helvetica-Oblique" => Some(StandardFont::HelveticaOblique),
            b"HeBO" => Some(StandardFont::HelveticaBoldOblique),
            b"Helvetica-BoldOblique" => Some(StandardFont::HelveticaBoldOblique),
            b"TiRo" => Some(StandardFont::TimesRoman),
            b"Times-Roman" => Some(StandardFont::TimesRoman),
            b"TiBo" => Some(StandardFont::TimesBold),
            b"Times-Bold" => Some(StandardFont::TimesBold),
            b"TiIt" => Some(StandardFont::TimesItalic),
            b"Times-Italic" => Some(StandardFont::TimesItalic),
            b"TiBI" => Some(StandardFont::TimesBoldItalic),
            b"Times-BoldItalic" => Some(StandardFont::TimesBoldItalic),
            b"Cour" => Some(StandardFont::Courier),
            b"Courier" => Some(StandardFont::Courier),
            b"CoBo" => Some(StandardFont::CourierBold),
            b"Courier-Bold" => Some(StandardFont::CourierBold),
            b"CoOb" => Some(StandardFont::CourierOblique),
            b"Courier-Oblique" => Some(StandardFont::CourierOblique),
            b"CoBO" => Some(StandardFont::CourierBoldOblique),
            b"Courier-BoldOblique" => Some(StandardFont::CourierBoldOblique),
            b"Symb" => Some(StandardFont::Symbol),
            b"Symbol" => Some(StandardFont::Symbol),
            b"ZaDb" => Some(StandardFont::ZapfDingbats),
            b"ZapfDingbats" => Some(StandardFont::ZapfDingbats),
            _ => None,
        }
    }

    /// Return the advance width of a glyph in 1/1000 em units.
    ///
    /// Performs a binary search over the sorted glyph table.
    /// Returns `None` if `glyph_name` is not present in this font's metrics.
    pub fn glyph_width(&self, glyph_name: &str) -> Option<u16> {
        let table = self.glyph_table();
        table
            .binary_search_by_key(&glyph_name, |&(name, _)| name)
            .ok()
            .map(|idx| table[idx].1)
    }

    /// Width of the `.notdef` glyph in 1/1000 em units.
    ///
    /// Most Core14 AFMs do not define `.notdef`; returns `0` in that case.
    pub fn notdef_width(&self) -> u16 {
        self.glyph_width(".notdef").unwrap_or(0)
    }

    /// Measure a WinAnsiEncoding byte string at the given font size.
    ///
    /// Each byte is mapped through [`WINANSI_ENCODING`] to a glyph name,
    /// then looked up in this font's width table.  Unmapped bytes (`.notdef`
    /// entries) and glyphs absent from the table both fall back to
    /// [`Self::notdef_width`].
    ///
    /// The returned value is in the same units as `font_size` (i.e. the
    /// advance width is scaled by `font_size / 1000`).
    pub fn string_width(&self, bytes: &[u8], font_size: f64) -> f64 {
        let notdef = f64::from(self.notdef_width());
        let total: f64 = bytes
            .iter()
            .map(|&b| {
                let glyph = WINANSI_ENCODING[b as usize];
                if glyph == ".notdef" {
                    notdef
                } else {
                    self.glyph_width(glyph)
                        .map(f64::from)
                        .unwrap_or(notdef)
                }
            })
            .sum();
        total * font_size / 1000.0
    }

    /// Return the static glyph-width table for this font.
    ///
    /// Entries are `(glyph_name, width_in_1000ths_em)`, sorted by glyph name.
    fn glyph_table(&self) -> &'static [(&'static str, u16)] {
        match self {
            StandardFont::Helvetica => GLYPHS_HELVETICA,
            StandardFont::HelveticaBold => GLYPHS_HELVETICABOLD,
            StandardFont::HelveticaOblique => GLYPHS_HELVETICAOBLIQUE,
            StandardFont::HelveticaBoldOblique => GLYPHS_HELVETICABOLDOBLIQUE,
            StandardFont::TimesRoman => GLYPHS_TIMESROMAN,
            StandardFont::TimesBold => GLYPHS_TIMESBOLD,
            StandardFont::TimesItalic => GLYPHS_TIMESITALIC,
            StandardFont::TimesBoldItalic => GLYPHS_TIMESBOLDITALIC,
            StandardFont::Courier => GLYPHS_COURIER,
            StandardFont::CourierBold => GLYPHS_COURIERBOLD,
            StandardFont::CourierOblique => GLYPHS_COURIEROBLIQUE,
            StandardFont::CourierBoldOblique => GLYPHS_COURIERBOLDOBLIQUE,
            StandardFont::Symbol => GLYPHS_SYMBOL,
            StandardFont::ZapfDingbats => GLYPHS_ZAPFDINGBATS,
        }
    }
}

